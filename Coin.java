public class Coin {
    private int val;
    private int quant;

    public Coin(int val, int quant) {
        this.val = val;
        this.quant = quant;
    }

    public void setQuant(int quant) {
        this.quant = quant;

    }

    public void setVal(int val) {
        this.val = val;
    }
}
